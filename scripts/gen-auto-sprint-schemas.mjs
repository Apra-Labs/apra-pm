#!/usr/bin/env node
// Regenerates the "role schemas" section of .claude/workflows/auto-sprint.js from
// the canonical, machine-readable role contracts at agents/schemas/<role>-output.json.
//
// Why this exists: .claude/workflows/auto-sprint.js is not a real Node.js file -- it
// is the script body Claude's Workflow tool executes directly in a sandboxed VM with
// NO require()/filesystem access (see the Workflow tool's own contract: "No filesystem
// or Node.js API access"). An earlier revision (apra-fleet-unw.21) assumed this file
// could do real I/O and loaded the schemas at runtime via require('fs')/require('path')
// -- that crashes immediately every time the workflow actually runs (`require is not
// defined`), which is exactly what took down the apra-pm e2e s10 suite on 2026-07-17
// (run 29605783512): the workflow had to be resumed twice just to work around it.
//
// The fix: inline the schemas as plain JS object literals at BUILD TIME, generated by
// this script, so agents/schemas/*.json stays the single source of truth and
// auto-sprint.js carries a checked-in, drift-free COPY with zero runtime fs dependency.
// Run this script and commit the result whenever an agents/schemas/<role>-output.json
// file changes.
//
// Also strips the schema files' top-level "version" key: it's a non-standard
// JSON-Schema keyword (not part of draft-07) that the agent tool's strict schema
// validator rejects when a schema is passed as a tool's `schema` option -- inlining it
// unstripped is bug #2 from the same s10 failure (validator rejection right after the
// require() crash was worked around).
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const targetFile = path.join(repoRoot, '.claude', 'workflows', 'auto-sprint.js');
const schemasDir = path.join(repoRoot, 'agents', 'schemas');

// role -> (schema file basename without -output.json, const name in auto-sprint.js)
const ROLES = [
  ['reviewer', 'REVIEW_SCHEMA'],
  ['plan-reviewer', 'PLAN_REVIEW_SCHEMA'],
  ['doer', 'DOER_STATUS_SCHEMA'],
  ['deployer', 'DEPLOYER_SCHEMA'],
  ['integ-test-runner', 'INTEG_RUN_SCHEMA'],
  ['ci-watcher', 'CI_SCHEMA'],
  ['harvester', 'HARVEST_SCHEMA'],
];

const BEGIN_MARKER = '// ROLE_SCHEMAS_GENERATED_BEGIN -- do not hand-edit; run `node scripts/gen-auto-sprint-schemas.mjs` to regenerate from agents/schemas/*.json';
const END_MARKER = '// ROLE_SCHEMAS_GENERATED_END';

function loadStrippedSchema(role) {
  const file = path.join(schemasDir, `${role}-output.json`);
  const parsed = JSON.parse(readFileSync(file, 'utf-8'));
  const { version, ...rest } = parsed;
  return rest;
}

function generateSection() {
  const lines = [
    BEGIN_MARKER,
    '//',
    '// apra-fleet-unw.21 / apra-fleet e2e s10 (2026-07-17): the role-contract schemas',
    '// below are generated from vendor/apra-pm\'s own canonical, machine-readable role',
    '// contracts at agents/schemas/<role>-output.json, instead of being hand-copied',
    '// inline literals -- this closes the drift this file used to have from the',
    '// vendored agents/*.md prose and from packages/apra-fleet-se/auto-sprint/contracts.mjs.',
    '// The "version" key present in each source file is dropped: it is a non-standard',
    '// JSON-Schema keyword the agent tool\'s strict schema validator rejects.',
    '//',
    '// Inlined at BUILD TIME (not loaded via require(\'fs\') at runtime) because this file',
    '// runs inside Claude\'s Workflow tool sandbox, which has no filesystem/require access',
    '// -- a prior runtime-load revision crashed on every invocation with "require is not',
    '// defined". See scripts/gen-auto-sprint-schemas.mjs, which generates this block.',
  ];
  for (const [role, constName] of ROLES) {
    const schema = loadStrippedSchema(role);
    lines.push(`const ${constName} = ${JSON.stringify(schema, null, 2)};`);
  }
  lines.push(END_MARKER);
  return lines.join('\n');
}

function main() {
  const src = readFileSync(targetFile, 'utf-8');
  const beginIdx = src.indexOf(BEGIN_MARKER);
  const endIdx = src.indexOf(END_MARKER);

  const newSection = generateSection();

  let next;
  if (beginIdx === -1 || endIdx === -1) {
    // First run: replace the legacy "------- role schemas" section (from its header
    // comment through the last `const X_SCHEMA = loadRoleSchema(...)` line) wholesale.
    const headerIdx = src.indexOf('// ------------------------------------------------------------- role schemas');
    const afterIdx = src.indexOf("const HARVEST_SCHEMA     = loadRoleSchema('harvester');");
    if (headerIdx === -1 || afterIdx === -1) {
      throw new Error('Could not locate the legacy role-schemas section to replace -- has auto-sprint.js changed shape? Edit this script\'s markers accordingly.');
    }
    const afterLineEnd = src.indexOf('\n', afterIdx) + 1;
    next = src.slice(0, headerIdx) + newSection + '\n' + src.slice(afterLineEnd);
  } else {
    const endOfEnd = endIdx + END_MARKER.length;
    next = src.slice(0, beginIdx) + newSection + src.slice(endOfEnd);
  }

  writeFileSync(targetFile, next, 'utf-8');
  console.log(`Regenerated role schemas in ${path.relative(repoRoot, targetFile)}`);
}

main();
